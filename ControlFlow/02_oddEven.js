// Javascript Program to Check if a Number is Odd or Even


function oddEven(num) {
    if (num % 2 === 0) {
        console.log(`${num} : Even number`);
    } else if (num % 2 !== 0) {
        console.log(`${num} : Odd Number`);
    }
}

let result1 = oddEven(12)
let result2 = oddEven(11)
let result3 = oddEven(24)
let result4 = oddEven(33)

console.log(result1);
console.log(result2);
console.log(result3);
console.log(result4);





